<p>Com o objetivo de elucidar os condicionantes estruturais do processo recente de regionalização nos estados, por meio da construção de uma 
tipologia nacional das regiões de saúde com base nas Comissões Intergestores Regionais - CIR formalmente constituídos até 12 de setembro de 2012.
Essa tipologia é baseada na tipologia criada em 2010 para os antigos Colegiados de Gestão Regional - CGR's 
que permitia observar os diversos graus de desenvolvimento econômico, social e características da rede de saúde dos municípios.</a><a href="#_ftn1" 
name="_ftnref1" title="">[1]</a></p>

<p>A nova tipologia apresentada mantém os parâmetros que nortearam a construção da antiga - no nível de CGR - mas apresenta uma operacionalização 
mais amigável a partir de um número menor de componentes. Essa simplificação operacional é importante dado às possíveis mudanças no desenho das 
CIR's que eventualmente venham a ocorrer ao longo do tempo. Destaca-se ainda a introdução de informações referentes ao Censo Demográfico 2010, que 
não estavam disponíveis no momento da construção da antiga tipologia.</p>

<p>Analogamente a tipologia anterior as CIR's foram classificadas, de forma independente, em cinco grupos de acordo com suas características 
socioeconômicas e de sistema de saúde.</p>

<p>Além da nova tipologia foram construídos bancos de dados tanto no nível de CIR quanto de municípios com informações relevantes para o gestor 
público da saúde. A construção desse banco de dados no nível municipal justifica-se na medida em que, a partir da unidade geográfica municipal 
torna-se possível a tabulação dos dados para os CIR, Unidades da Federação, Grandes Regiões e Brasil.</p>

<p>A seguir descrevem-se os procedimentos utilizados na construção da tipologia das CIR's.</p>

<p><b>METODOLOGIA</b></p>

<p>Tendo em vista o caráter multidisciplinar da área de saúde, a construção da tipologia de CIR exigiu a exploração de um conjunto diversificado de 
fontes de dados. A pesquisa inicial de fontes de dados de interesse e a opção final pelo uso de um conjunto de fontes de dados seguiram cinco 
diretrizes: <i>a)</i> dados válidos, consistentes e confiáveis; <i>b)</i> dados largamente aceitos e reconhecidos pelas respectivas áreas técnicas; 
<i>c)</i> dados padronizados, com série histórica e atualizados periodicamente; <i>d)</i> dados com cobertura nacional e que permitissem a sua 
desagregação a nível municipal; <i>e)</i> dados de preferência de acesso público e que permitissem um pareamento mínimo a nível municipal.</p>

<p>A partir desses pontos, as fontes de dados utilizadas foram o Censo Demográfico 2010 realizado pelo Instituto Brasileiro de Geografia e 
Estatística - IBGE; os Bancos de Dados do Sistema Único de Saúde disponíveis no Datasus (<a 
href="http://www.datasus.gov.br/" target="_blank">www.datasus.gov.br</a>); a Relação Anual de Informações Sociais - RAIS, disponibilizado anualmente pelo Ministério 
do Trabalho (<a href="http://www.mte.gov.br/" target="_blank">www.mte.gov.br</a>) e o Sistema de Contas Regionais - IBGE.</p>

<p>As unidades de análise foram as Comissões Intergestores Regionais - CIR, a partir da agregação de dados municipais e microdados do Censo 
Demográfico 2010. Foram consideradas 431 CIR que englobam os 5.565 municípios do Brasil.</p>

<p>Foram introduzidas alterações na construção da tipologia atual em relação a anterior. As alterações foram:</p>

<ul>
    <li>Redução do número de componentes no indicador: essa redução permitiu maior simplicidade na sua operacionalização, sem perda de conteúdo 
analítico.</li>
    <li>Atualização das informações a serem utilizadas para o ano de 2010. A exceção são as informações provenientes das Contas Regionais que são 
referentes a 2009.</li>
    <li>Substituição das informações provenientes do Índice de Desenvolvimento Humano Municipal - IDH-M por informações provenientes do Censo 
Demográfico 2010.</li>
</ul>
<p>Analogamente a tipologia de CGR a estratégia analítica foi identificar os vários tipos de situações sociais e de condições de saúde existentes na 
região, por meio do cruzamento das dimensões <i>Situação Socioeconômica</i> e <i>Oferta e Complexidade dos Serviços de Saúde</i> (Quadro 1).</p>

