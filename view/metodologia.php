<p>A identificação das dimensões e dos grupos - que compõem a tipologia - foi realizada por meio das técnicas estatísticas: análise fatorial e 
análise de agrupamentos.<a href="#_ftn2" name="_ftnref2" title="">[2]</a></p>

<p>A análise fatorial consiste em uma técnica estatística de análise multivariada que se aplica à identificação de fatores que apontem objetivamente 
para a agregação de um conjunto de medidas. Uma vez identificados os fatores, cabe ao pesquisador verificar se tais fatores são coerentes e 
consistentes em relação à natureza dos fenômenos ou processos estudados. Esta técnica é frequentemente utilizada na resolução de problemas 
envolvendo um grande número de variáveis, onde se deseja a redução deste número com a finalidade de facilitar o entendimento analítico dos 
dados.</p>

<p>Já a análise de agrupamentos identifica as unidades de análise, no caso CGR, com perfis semelhantes segundo um conjunto de variáveis. No presente 
caso, as variáveis foram os escores fatoriais gerados na análise fatorial. Essa análise é muito utilizada para fins de análise regional, onde se 
procura observar os padrões de semelhança ou diferença entre áreas que estão sendo comparadas, tais como municípios ou agrupamentos de 
municípios.</p>

<p>A operacionalização das duas dimensões Situação Socioeconômica e Oferta e Complexidade dos Serviços da Saúde pela analise fatorial, podem ser 
descritas como:</p>

<ul>
<li><u>Situação Socioeconômica</u>: relacionada ao grau de desenvolvimento socioeeconômico dos municípios pertencentes as CIR. As CIR que atingem os 
maiores valores nesse indicador caracterizam-se por agruparem os municípios mais urbanizados, populosos, industrializados e dinâmicos 
economicamente.</li>
</ul>

<ul>
<li><u>Oferta e Complexidade dos Serviços de Saúde</u>: relacionada à complexidade dos serviços ofertados no CGR, maiores valores nesse fator indicam maior 
oferta e complexidade do sistema de saúde.</li>
</ul>

<p>Na construção da nova tipologia os dois fatores foram classificados em três categorias, definidas relativamente ao conjunto dos dados:</p>

<ul>
<li><u>Situação Socioeconômica</u>: Baixa (1,707 a -0,3515), Média (-0,3514 a 0,8509) Alta (0,8510 a 3,5070);<a href="#_ftn3" name="_ftnref3" title="">[3]
</a></li>
<li><u>Oferta e Complexidade dos Serviços de Saúde</u>: Baixa (-1,2133 a -01250), Média (-01249 a 0,9140) e Alta (0,9141 a 2,3751).<a href="#_ftn4" 
name="_ftnref4" title="">[4]</a></li>
</ul>

<p>O cruzamento desses fatores gerou cinco grupos:</p>

<ul>
<li><b>Grupo 1 (baixo desenvolvimento socioeconômico e baixa oferta de serviços):</b> inclui 178 CIRs, 2.172 municípios e 22,8% da população do 
Brasil no ano de 2010. 82% dessas CIRs estão localizadas nas regiões Norte e Nordeste (Tabela 1).</li>

<li><b>Grupo 2 (médio/alto desenvolvimento socioeconômico e baixa oferta de serviços):</b> inclui 56 CIRs, 574 municípios e 6,6 da população do 
Brasil no ano de 2010. 89% dessas CIRs estão localizadas nas regiões Norte, Centro-Oeste e Sudeste (Norte de Minas e Vale do Ribeira em São Paulo) 
(Tabela 1).</li>

<li><b>Grupo 3 (médio desenvolvimento socioeconômico e média oferta de serviços):</b> inclui 108 CIRs, 1.706 municípios e 18,5% da população do 
Brasil no ano de 2010. 84% dessas CIRs estão localizadas nas Sudeste e Sul (Tabela 1).</li>

<li><b>Grupo 4 (alto desenvolvimento socioeconômico e média oferta de serviços):</b> inclui 46 CIRs, 548 municípios e 14,8% da população do Brasil 
no ano de 2010. 83% dessas CIRs estão localizadas nas Sudeste e Sul (Tabela 1).</li>

<li><b>Grupo 5 (alto desenvolvimento socioeconômico e alta oferta de serviços):</b> inclui 43 CIRs, 565 municípios e 37,4% da população do Brasil 
no ano de 2010. 70% dessas CIRs estão localizadas nas Sudeste e Sul (Tabela 1).</li>
</ul>
